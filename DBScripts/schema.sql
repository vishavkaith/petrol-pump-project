-- Database schema for Petrol Pump Management System

-- Create PETROLPUMP table
CREATE TABLE IF NOT EXISTS PETROLPUMP (
    ID INTEGER PRIMARY KEY AUTOINCREMENT,
    NAME TEXT NOT NULL,
    ADDRESS TEXT,
    CITY TEXT,
    PINCODE TEXT,
    REGISTRATION TEXT,
    EMAIL TEXT,
    PHONE TEXT,
    STATE TEXT,
    CREATED_AT DATETIME DEFAULT CURRENT_TIMESTAMP
);

-- Create USER table
CREATE TABLE IF NOT EXISTS USER (
    ID INTEGER PRIMARY KEY AUTOINCREMENT,
    PID INTEGER,
    FNAME TEXT,
    LNAME TEXT,
    ADDRESS TEXT,
    PHONE TEXT,
    EMAIL TEXT,
    ADHAAR TEXT,
    PASSWORD TEXT,
    PIC TEXT,
    ACTIVE INTEGER DEFAULT 1,
    CREATED_AT DATETIME DEFAULT CURRENT_TIMESTAMP,
    FOREIGN KEY (PID) REFERENCES PETROLPUMP(ID)
);

-- Create ROLE table
CREATE TABLE IF NOT EXISTS ROLE (
    ID INTEGER PRIMARY KEY AUTOINCREMENT,
    TYPE TEXT NOT NULL,
    PID INTEGER,
    CREATED_AT DATETIME DEFAULT CURRENT_TIMESTAMP,
    FOREIGN KEY (PID) REFERENCES PETROLPUMP(ID)
);

-- Create USER_ROLE table
CREATE TABLE IF NOT EXISTS USER_ROLE (
    ID INTEGER PRIMARY KEY AUTOINCREMENT,
    PID INTEGER,
    USER_ID INTEGER,
    ROLE_ID INTEGER,
    CREATED_AT DATETIME DEFAULT CURRENT_TIMESTAMP,
    FOREIGN KEY (PID) REFERENCES PETROLPUMP(ID),
    FOREIGN KEY (USER_ID) REFERENCES USER(ID),
    FOREIGN KEY (ROLE_ID) REFERENCES ROLE(ID)
);

-- Create COLLECTION_MODE table
CREATE TABLE IF NOT EXISTS COLLECTION_MODE (
    ID INTEGER PRIMARY KEY AUTOINCREMENT,
    PID INTEGER,
    MODE TEXT NOT NULL,
    CREATED_AT DATETIME DEFAULT CURRENT_TIMESTAMP,
    FOREIGN KEY (PID) REFERENCES PETROLPUMP(ID)
);

-- Create PRODUCT table
CREATE TABLE IF NOT EXISTS PRODUCT (
    ID INTEGER PRIMARY KEY AUTOINCREMENT,
    PID INTEGER NOT NULL,
    NAME TEXT NOT NULL,
    
    -- 0 = Non-Fuel, 1 = Fuel
    TYPE INTEGER NOT NULL CHECK (TYPE IN (0,1)),

    PRICE REAL DEFAULT 0,

    -- If 1 = Stock managed via inventory ledger
    -- If 0 = Ignore stock (services, etc.)
    MANAGE_STOCK INTEGER DEFAULT 1 CHECK (MANAGE_STOCK IN (0,1)),

    ACTIVE INTEGER DEFAULT 1 CHECK (ACTIVE IN (0,1)),

    CREATED_AT DATETIME DEFAULT CURRENT_TIMESTAMP,

    FOREIGN KEY (PID) REFERENCES PETROLPUMP(ID)
);
-- Create TANK_METADATA table
CREATE TABLE IF NOT EXISTS TANK_METADATA (
    ID INTEGER PRIMARY KEY AUTOINCREMENT,
    PID INTEGER,
    CAPACITY REAL,
    TYPE TEXT,
    ACTIVE INTEGER DEFAULT 1,
    CREATED_AT DATETIME DEFAULT CURRENT_TIMESTAMP,
    FOREIGN KEY (PID) REFERENCES PETROLPUMP(ID)
);

-- Create NOZZLE table
CREATE TABLE IF NOT EXISTS NOZZLE (
    ID INTEGER PRIMARY KEY AUTOINCREMENT,
    PID INTEGER,
    NOZZLE_NUMBER INTEGER,
    TYPE TEXT,
    ACTIVE INTEGER DEFAULT 1,
    CREATED_AT DATETIME DEFAULT CURRENT_TIMESTAMP,
    FOREIGN KEY (PID) REFERENCES PETROLPUMP(ID)
);

-- Create SHIFT_METADATA table
CREATE TABLE IF NOT EXISTS SHIFT_METADATA (
    ID INTEGER PRIMARY KEY AUTOINCREMENT,
    PID INTEGER,
    STARTTIME TEXT,
    ENDTIME TEXT,
    SHIFT_TYPE TEXT,
    SID INTEGER,
    ACTIVE INTEGER DEFAULT 1,
    CREATED_AT DATETIME DEFAULT CURRENT_TIMESTAMP,
    FOREIGN KEY (PID) REFERENCES PETROLPUMP(ID),
    FOREIGN KEY (SID) REFERENCES USER(ID)
);

-- Create SHIFT table
CREATE TABLE IF NOT EXISTS SHIFT (
    ID INTEGER PRIMARY KEY AUTOINCREMENT,
    PID INTEGER,
    SHIFT_META_DATA_ID INTEGER,
    SUPERVISOR_ID INTEGER,
    START_TIME DATETIME,
    END_TIME DATETIME,
    CREATED_AT DATETIME DEFAULT CURRENT_TIMESTAMP,
    FOREIGN KEY (PID) REFERENCES PETROLPUMP(ID),
    FOREIGN KEY (SHIFT_META_DATA_ID) REFERENCES SHIFT_METADATA(ID),
    FOREIGN KEY (SUPERVISOR_ID) REFERENCES USER(ID)
);

-- Create SHIFT_NOZZLE_DSM table
CREATE TABLE IF NOT EXISTS SHIFT_NOZZLE_DSM (
    ID INTEGER PRIMARY KEY AUTOINCREMENT,
    PID INTEGER,
    SHIFT_ID INTEGER,
    NOZZLE_ID INTEGER,
    USER_ID INTEGER,
    DOR REAL DEFAULT 0,
    DCR REAL DEFAULT 0,
    AOR REAL DEFAULT 0,
    ACR REAL DEFAULT 0,
    PUMP_TEST REAL DEFAULT 0,
    SELF REAL DEFAULT 0,
    CREATED_AT DATETIME DEFAULT CURRENT_TIMESTAMP,
    FOREIGN KEY (PID) REFERENCES PETROLPUMP(ID),
    FOREIGN KEY (SHIFT_ID) REFERENCES SHIFT(ID),
    FOREIGN KEY (NOZZLE_ID) REFERENCES NOZZLE(ID),
    FOREIGN KEY (USER_ID) REFERENCES USER(ID)
);

-- Create SHIFT_COLLECTION table
CREATE TABLE IF NOT EXISTS SHIFT_COLLECTION (
    ID INTEGER PRIMARY KEY AUTOINCREMENT,
    SHIFT_NOZZLE_DSM_ID INTEGER,
    COLLECTION_MODE_ID INTEGER,
    AMOUNT REAL,
    PID INTEGER,
    CREATED_AT DATETIME DEFAULT CURRENT_TIMESTAMP,
    FOREIGN KEY (SHIFT_NOZZLE_DSM_ID) REFERENCES SHIFT_NOZZLE_DSM(ID),
    FOREIGN KEY (COLLECTION_MODE_ID) REFERENCES COLLECTION_MODE(ID),
    FOREIGN KEY (PID) REFERENCES PETROLPUMP(ID)
);

-- Create TANK table
CREATE TABLE IF NOT EXISTS TANK (
    ID INTEGER PRIMARY KEY AUTOINCREMENT,
    PID INTEGER,
    TANK_ID INTEGER,
    SHIFT_ID INTEGER,
    OPEN_READING REAL DEFAULT 0,
    CLOSE_READING REAL DEFAULT 0,
    FUEL_RECEIVED REAL DEFAULT 0,
    CREATED_AT DATETIME DEFAULT CURRENT_TIMESTAMP,
    FOREIGN KEY (PID) REFERENCES PETROLPUMP(ID),
    FOREIGN KEY (TANK_ID) REFERENCES TANK_METADATA(ID),
    FOREIGN KEY (SHIFT_ID) REFERENCES SHIFT(ID)
);

-- Create SHIFT_PRICE table
CREATE TABLE  IF NOT EXISTS SHIFT_PRICE (
    ID INTEGER PRIMARY KEY AUTOINCREMENT,
    PID INTEGER,
    SID INTEGER,
    PRO_ID INTEGER,
    PRICE REAL,
    QTY INTEGER,
    CREATED_AT DATETIME DEFAULT CURRENT_TIMESTAMP,
    FOREIGN KEY (PID) REFERENCES PETROLPUMP(ID),
    FOREIGN KEY (SID) REFERENCES SHIFT(ID),
    FOREIGN KEY (PRO_ID) REFERENCES PRODUCT(ID)
);

