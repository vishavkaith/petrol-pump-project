-- Create DATASHEET table

CREATE TABLE DATASHEET (
  ID INTEGER PRIMARY KEY AUTOINCREMENT,
  TANK_METADATA_ID INTEGER NOT NULL,
  HEIGHT INTEGER NOT NULL,
  CMV REAL NOT NULL,
  MMV REAL NOT NULL,
  UNIQUE (TANK_METADATA_ID, HEIGHT),
  FOREIGN KEY (TANK_METADATA_ID) REFERENCES TANK_METADATA(ID)
);-- FUEL_RATE table (SQLite)

CREATE TABLE IF NOT EXISTS FUEL_RATE (
    ID INTEGER PRIMARY KEY AUTOINCREMENT,
    PID INTEGER NOT NULL,
    PRODUCT_TYPE TEXT NOT NULL CHECK (PRODUCT_TYPE IN ('HSD','MS','EBMS')),
    RATE REAL NOT NULL,
    EFFECTIVE_FROM DATE NOT NULL,
    UNIQUE (PID, PRODUCT_TYPE, EFFECTIVE_FROM)
);

-- INVENTORY_LEDGER table (SQLite)
CREATE TABLE IF NOT EXISTS INVENTORY_LEDGER (
    ID INTEGER PRIMARY KEY AUTOINCREMENT,
    PID INTEGER NOT NULL,
    PRODUCT_TYPE TEXT NOT NULL,
    TRANSACTION_DATE DATETIME NOT NULL,
    ENTRY_DATE DATETIME DEFAULT CURRENT_TIMESTAMP,
    SOURCE_TYPE TEXT NOT NULL CHECK (
        SOURCE_TYPE IN (
            'OPENING',
            'SHIFT_SALE',
            'PURCHASE',
            'ADJUSTMENT',
            'TRANSFER_IN',
            'TRANSFER_OUT',
            'POS_SALE'
        )
    ),
    SOURCE_ID INTEGER,
    QTY REAL NOT NULL,
    RATE REAL DEFAULT 0,
    AMOUNT REAL DEFAULT 0,
    REMARK TEXT,
    CREATED_BY INTEGER
);

CREATE INDEX IF NOT EXISTS IDX_LEDGER_PID_PRODUCT_DATE
ON INVENTORY_LEDGER (PID, PRODUCT_TYPE, TRANSACTION_DATE);
